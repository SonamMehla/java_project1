public class MyCalculator {

    public int sum(int a,int b){
        int sum = a+b;
        return sum;
    }
    public int subtract(int a,int b){
        int subtract = a-b;
        return subtract;
    }
    public int multiplication(int a,int b){
        int mul = a*b;
        return mul;
    }
    public int divide(int a,int b){
        int div = a-b;
        return div;
    }
    public int module(int a,int b){
        int mod = a-b;
        return mod;
    }
}
